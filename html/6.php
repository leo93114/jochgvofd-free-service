<h1>加入水平分線</h1>
<fieldset>
如果想要在網頁內加入一條像下面的水平線，加入&lt;hr&gt;的碼即可。<br />
<hr size="5" align="center" noshade width="90%" color="#9900ff"><br />
這是上面那一條線的原始碼 :<br />
&lt;hr <span style="color:#cc00cc;">size</span>=&quot;<span style="color:#9900ff;">5</span>&quot; <span style="color:#cc00cc;">align</span>=&quot;<span style="color:#9900ff;">center</span>&quot; <span style="color:#cc00cc;">noshade width</span>=&quot;<span style="color:#9900ff;">90%</span>&quot; <span style="color:#cc00cc;">color</span>=&quot;<span style="color:#9900ff;">#9900ff</span>&quot;&gt;
<div>
	<span style="color:#cc00cc;">size</span>=控制線的寬度</div>
<div>
	<span style="color:#cc00cc;">align</span>=控制線是靠左(left)／靠右(right)／中間(center)</div>
<div>
	<span style="color:#cc00cc;">width</span>=控制線的長度，可用數字或百分比</div>
<div>
	<span style="color:#cc00cc;">noshade</span>=控制線沒有陰影</div>
<div>
	<span style="color:#cc00cc;">color</span>=控制線的顏色</div>
